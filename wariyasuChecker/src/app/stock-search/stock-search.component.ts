import { Component, OnInit } from '@angular/core';
import { CaluculateService } from './shared/caluculate.service';
import { LatestStockService } from './shared/latest-stock.service';

@Component({
  selector: 'app-stock-search',
  templateUrl: './stock-search.component.html',
  styleUrls: ['./stock-search.component.scss']
})
export class StockSearchComponent implements OnInit {
  // パネル開閉
  panelOpenState = true;

  // sectorID
  sectorID: string = '';

  // 株価計算結果
  calucurateResult: any;

  // 初期取得List
  initialList: any;

  // table表示用List
  displayList: any;

  constructor(
    private caluculateService: CaluculateService,
    private latestStockService: LatestStockService
  ) { }

  ngOnInit(): void {
    // this.caluculateService.caluculateForDisplay().then(res => {
    //   this.calucurateResult = res;
    // });
  }

  searchStocks(ticker:string){
    this.panelOpenState = false;

    // 初回取得
    if(this.displayList == null){
      // this.initialList = this.caluculateService.margeWithAveList(this.calucurateResult);
      this.initialList = VALUE;
    }

    if(this.sectorID == '' && ticker == ''){
      this.displayList = this.initialList;
    }else{
      let result = this.initialList;
      if(this.sectorID != ''){
        result = result.filter(res => res.sector == this.sectorID);
      }
      if(ticker != ''){
        result = result.filter(res => res.ticker == ticker);
      }

      this.displayList = result;
    }
  }

  // sectorセレクトボックス選択
  changeSector(value: string){
    this.sectorID = value;
  }

  clearSearch(){
    this.panelOpenState = true;
  }

  clickTest(){
    this.latestStockService.getLatestStockList();
  }
}

